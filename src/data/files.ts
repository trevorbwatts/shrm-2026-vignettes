export const fileCategories = [
  { id: 'all', label: 'All files', count: files.length },
  { id: 'signature-templates', label: 'Signature Templates', count: files.filter(f => f.category === 'Signature Templates').length },
  { id: 'benefits-docs', label: 'Benefits Docs', count: files.filter(f => f.category === 'Benefits Docs').length },
  { id: 'payroll', label: 'Payroll', count: files.filter(f => f.category === 'Payroll').length },
  { id: 'trainings', label: 'Trainings', count: files.filter(f => f.category === 'Trainings').length },
  { id: 'company-policies', label: 'Company Policies', count: files.filter(f => f.category === 'Company Policies').length },
];

export const getCategoryLabel = (id: string): string => {
  const category = fileCategories.find(c => c.id === id);
  return category?.label || 'All Files';
};

export const getFilesByCategory = (categoryId: string): File[] => {
  if (categoryId === 'all') return files;
  const categoryLabel = getCategoryLabel(categoryId);
  return files.filter(f => f.category === categoryLabel);
};
